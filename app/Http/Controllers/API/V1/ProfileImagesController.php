    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $profileImage = ProfileImage::findOrFail($id);
        if(!empty($profileImage->profile_image_path)) {
            $name = basename($profileImage->profile_image_path);
            Storage::disk('uploads')->delete("profile/$profileImage->profile_id/$name");
        }
        $profileImage->delete();

        return response()->json(['error' => false, 'data' => $profileImage]);
    }
